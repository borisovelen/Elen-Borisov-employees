public class Pairs {
    private int emp1;
    private int emp2;
    private long days;

    public Pairs(int emp1, int emp2, long days) {
        this.emp1 = emp1;
        this.emp2 = emp2;
        this.days = days;
    }

    public int getEmp1() {
        return emp1;
    }

    public void setEmp1(int emp1) {
        this.emp1 = emp1;
    }

    public int getEmp2() {
        return emp2;
    }

    public void setEmp2(int emp2) {
        this.emp2 = emp2;
    }

    public long getDays() {
        return days;
    }

    public void setDays(long days) {
        this.days = days;
    }
}
